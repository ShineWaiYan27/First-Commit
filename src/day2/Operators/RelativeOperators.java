package day2.Operators;

public class RelativeOperators {
    public static void main(String[] args) {
        int n1 = 3;
        int n2 = 4;

        System.out.println(n1 > n2);
        System.out.println(n1 < n2);
        System.out.println(n1 == n2);
        System.out.println(n1 != n2);
    }
}
